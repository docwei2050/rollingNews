public class NewsBean  {
    private String headLinePrev;
    private String headLineNext;
    private String imageUrl;

    public String getHeadLinePrev() {
        return headLinePrev;
    }

    public void setHeadLinePrev(String headLinePrev) {
        this.headLinePrev = headLinePrev;
    }

    public String getHeadLineNext() {
        return headLineNext;
    }

    public void setHeadLineNext(String headLineNext) {
        this.headLineNext = headLineNext;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }
}
